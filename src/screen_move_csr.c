#include "screen.h"
#include "port_io.h"

void move_csr(void) {
	uint32_t temp;
	temp = screen_vid.csr_y * 80 + screen_vid.csr_x;
	outportb(0x3D4, 14);
	outportb(0x3D5, temp >> 8);
	outportb(0x3D4, 15);
	outportb(0x3D5, temp);
}

