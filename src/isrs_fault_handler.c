void fault_handler(struct regs *r) {
	if(r->int_no < 32) {
		puts((uint8_t *)exception_messages[r->int_no]);
		int8_t *txt = " Exception. System Halted!\n";
		puts((uint8_t *)txt);
		for(;;);
	}
}

